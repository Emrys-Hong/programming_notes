# Create a memoisation decorator
def memoise(f):
    memo = {}
    def checker(x):
        if x not in memo:            
            memo[x] = f(x)
        return memo[x]
    return checker
# And apply it! Done!
@memoise
def fib(num):
    if num == 0:
        return 0
    elif num == 1:
        return 1
    else:
        return fib(num - 1) + fib(num - 2)
